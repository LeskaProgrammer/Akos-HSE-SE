#!/usr/bin/env bash
# Скрипт проверяет, существует ли указанный файл и доступен ли он для чтения.

FILE="$1"

if [[ -z "$FILE" ]]; then
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

if [[ -e "$FILE" && -r "$FILE" ]]; then
    echo "Файл '$FILE' существует и доступен для чтения."
elif [[ -e "$FILE" ]]; then
    echo "Файл '$FILE' существует, но недоступен для чтения."
else
    echo "Файл '$FILE' не существует."
fi
